const wchar_t* errorCodeName(DWORD code)
{
  static const wchar_t* NotFound = L"unknown error";

  auto itor = std::lower_bound(std::begin(g_codes), std::end(g_codes), CodeName{code},
                               [](auto&& a, auto&& b) {
                                 return (a.code < b.code);
                               });

  if (itor != std::end(g_codes)) {
    return itor->name;
  }

  return NotFound;
}

